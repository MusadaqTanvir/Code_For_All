#Organizing the List...
My_List = ['Realme','Samsung','Vivo','Google Pixel']
#First of all we will sort the list permanently using sort() method..
print("The Original List\n", My_List)
#Sorting the List...
My_List.sort()
#printing the output
print("The Sorted List\n", My_List)
#printing the list in the reverse order not sorted
My_List.reverse()
print("The List in the Reverse Order\n",My_List)
#Finding the length of the List...
print(len(My_List))



